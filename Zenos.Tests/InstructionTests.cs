using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using Xunit;
using Zenos.Framework;

namespace Zenos.Tests
{
    public class InstructionTests
    {
        [Fact]
        public void AddingAnInstructionShouldAddToTheEnd()
        {
            var ins1 = new ZenosInstruction();
            var ins2 = new ZenosInstruction();
            var ins3 = new ZenosInstruction();

            var last = ins1.Add(ins2);

            Assert.Same(ins2, last);
            Assert.Same(ins2, ins1.Next);

            last = ins1.Add(ins3);

            Assert.Same(ins3, last);
            Assert.Same(ins2, ins1.Next);
            Assert.Same(ins1, ins2.Previous);
            Assert.Same(ins3, ins2.Next);
            Assert.Same(ins2, ins3.Previous);
            Assert.Null(ins3.Next);
        }

        [Fact]
        public void AddingANullInstructionReturnLastInstruction()
        {
            var ins1 = new ZenosInstruction();
            var ins2 = new ZenosInstruction();

            var last = ins1.Add(ins2);

            Assert.Same(ins2, last);
            Assert.Same(ins2, ins1.Next);

            last = ins1.Add(null);

            Assert.Same(ins2, last);
            Assert.Same(ins2, ins1.Next);
            Assert.Null(ins2.Next);
        }
    }
}
